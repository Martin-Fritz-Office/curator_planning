      <div class="card-head">
        <h2>Page 2 · Priority actions and implementation</h2>
      </div>
      <div class="card-body">
        <p>Several strategic priorities follow from the findings. First, compensation frameworks should be fair and transparent: fees and project pay need to be calculable, comparable, and inclusive of hidden labor inputs. Second, funding mechanisms should be designed to avoid reproducing precarity through unrealistic budgets. This includes viable staffing lines, baseline standards in calls, and explicit equality criteria in grant assessment.</p>

        <p>Third, institutions benefit from embedding equality governance in routine management practice. This can include target corridors for leadership roles, transparent hiring and appointment procedures, periodic salary/function audits, and independent feedback or complaint channels. Measures are most effective when they are institutionalized as ongoing accountability systems rather than one-off initiatives.</p>

        <p>Fourth, compatibility-oriented working structures matter. Scheduling logic, production rhythms, and mobility expectations should not automatically penalize people with care responsibilities. In project-based environments, even moderate increases in planning reliability can improve retention and career continuity, for example through earlier schedule transparency, clear substitution rules, and more predictable contract formats.</p>

        <p>Fifth, the report emphasizes intersectional implementation. Gender equality should be linked with other dimensions of inequality so that policies reach not only majority profiles but also people facing multiple barriers. This includes access to networks, language accessibility in funding communication, regional disparities, and representation of marginalized perspectives in programming.</p>

        <p>In practical terms, durable progress comes less from symbolic gestures and more from continuous data-informed steering. Cultural institutions, funders, and policymakers can achieve the strongest impact by combining transparency, budget fairness, and accountability. The report provides a strong policy basis and demonstrates that measurable progress is possible when structural levers are addressed consistently.</p>

