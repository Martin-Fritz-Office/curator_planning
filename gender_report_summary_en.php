        <h2>Page 1 · Key findings</h2>
      </div>
      <div class="card-body">
        <p>The 2017–2021 Gender Report for arts and culture indicates clear progress in visibility and participation, while structural inequalities remain persistent in income, employment security, decision-making power, and long-term social protection. Women are strongly represented across many cultural occupations, but this presence does not automatically translate into equal pay, equal access to leadership, or comparable career stability. The report frames gender equality as a structural issue rather than an individual one, closely tied to funding design, institutional routines, and project-based labor conditions.</p>

        <p>A recurring pattern is the gap between participation and resource allocation. In fields with high female participation, careers are more often characterized by part-time work, short-term contracts, multi-job constellations, and volatile earnings. This increases exposure to precarious work, weak pension trajectories, and financial insecurity over the life course. The report also highlights that unpaid or underpaid labor (such as preparation, follow-up, communication, and networking work) is often unevenly distributed and insufficiently reflected in fee structures and project budgets.</p>

        <p>Leadership and decision-making levels show another persistent asymmetry. While gender balance is often stronger in education pipelines and early-career cohorts, female representation tends to decrease in management, jury participation, and high-budget gatekeeping functions. This points to structural barriers in career progression: informal recruitment channels, unequal care expectations, and inherited network effects. The report suggests that improving entry-level access alone is not enough; transparent progression pathways are needed across the full career ladder.</p>

        <p>Representation in public-facing cultural output is similarly uneven. Visibility in programs, exhibitions, productions, and media coverage is not distributed neutrally. Groups that are already institutionally anchored are more likely to receive reach, repeat commissions, and better-funded opportunities, creating cumulative advantage over time. In this perspective, gender bias is not only a matter of single decisions but of repeated patterns across curation, budgeting, and platform access.</p>

        <p>Methodologically, the report stresses that broad averages can hide important subgroup differences. Effective equality policy therefore requires disaggregated data (for example by gender, contract type, role level, and fee structure), regular monitoring cycles, and transparent reporting standards. Without this, progress is hard to evaluate and targeted interventions cannot be calibrated well.</p>
