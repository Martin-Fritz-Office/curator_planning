        <h2 id="vgo-intro">Was ist eine Verwertungsgesellschaft?</h2>
        <p>
          Verwertungsgesellschaften (VGs) sammeln Rechte von Urheber:innen und Leistungsschutzberechtigten,
          vergeben Lizenzen (z. B. für Nutzung in Medien, Veranstaltungen oder Online-Angeboten)
          und schütten Einnahmen an ihre Mitglieder aus. Für Einzelne wäre diese Rechtewahrnehmung oft zu aufwendig –
          die VG übernimmt sie gebündelt.
        </p>
        <p>
          Merke: Welche VG zuständig ist, hängt vom Werktyp ab (Musik, Text, Bild, Film etc.) und davon,
          ob es um Urheberrechte oder Leistungsschutzrechte geht.
        </p>
      </section>

      <section class="story-intro" aria-labelledby="vgo-overview">
        <h2 id="vgo-overview">Überblick aller zentralen Verwertungsgesellschaften in Österreich</h2>

        <article class="law-question">
          <h3>AKM (Autoren, Komponisten und Musikverleger)</h3>
          <p><strong>Zuständigkeit:</strong> Öffentliche Aufführung, Sendung und weitere Nutzungen von Musikwerken mit Text oder ohne Text.</p>
          <p><strong>Für wen?</strong> Komponist:innen, Songwriter:innen, Textautor:innen, Musikverlage.</p>
          <p><a href="https://www.akm.at" target="_blank" rel="noopener noreferrer">https://www.akm.at</a></p>
        </article>

        <article class="law-question">
          <h3>austro mechana</h3>
          <p><strong>Zuständigkeit:</strong> Mechanische Rechte an Musik (z. B. Vervielfältigung, digitale Nutzungen, Speichermedienvergütung in einschlägigen Bereichen).</p>
          <p><strong>Für wen?</strong> Urheber:innen und Verlage im Musikbereich, oft ergänzend zur AKM-Wahrnehmung.</p>
          <p><a href="https://www.austromechana.at" target="_blank" rel="noopener noreferrer">https://www.austromechana.at</a></p>
        </article>

        <article class="law-question">
          <h3>LSG (Wahrnehmung von Leistungsschutzrechten)</h3>
          <p><strong>Zuständigkeit:</strong> Leistungsschutzrechte, insbesondere von Interpret:innen und Tonträgerhersteller:innen (z. B. bei Sendung/öffentlicher Wiedergabe von Aufnahmen).</p>
          <p><strong>Für wen?</strong> Ausübende Künstler:innen und Produzent:innen im Tonträgerbereich.</p>
          <p><a href="https://www.lsg.at" target="_blank" rel="noopener noreferrer">https://www.lsg.at</a></p>
        </article>

        <article class="law-question">
          <h3>VDFS</h3>
          <p><strong>Zuständigkeit:</strong> Leistungsschutzrechte von Filmschaffenden und ausübenden Künstler:innen im audiovisuellen Bereich.</p>
          <p><strong>Für wen?</strong> Darsteller:innen, Mitwirkende und weitere Rechteinhaber:innen in Film/TV-Kontexten.</p>
          <p><a href="https://www.vdfs.at" target="_blank" rel="noopener noreferrer">https://www.vdfs.at</a></p>
        </article>

        <article class="law-question">
          <h3>VAM (Verwertungsgesellschaft für audiovisuelle Medien)</h3>
          <p><strong>Zuständigkeit:</strong> Rechtewahrnehmung für Filmhersteller:innen und Produzent:innen im audiovisuellen Bereich.</p>
          <p><strong>Für wen?</strong> Produzent:innen und Hersteller:innen audiovisueller Werke.</p>
          <p><a href="https://www.vam.cc" target="_blank" rel="noopener noreferrer">https://www.vam.cc</a></p>
        </article>

        <article class="law-question">
          <h3>Literar-Mechana</h3>
          <p><strong>Zuständigkeit:</strong> Rechte aus Texten und Sprachwerken, u. a. Reprographie-/Kopiervergütungen und weitere Nutzungsfelder im Literaturbereich.</p>
          <p><strong>Für wen?</strong> Autor:innen, Journalist:innen, Verlage und sonstige Rechteinhaber:innen an Sprachwerken.</p>
          <p><a href="https://www.literar.at" target="_blank" rel="noopener noreferrer">https://www.literar.at</a></p>
        </article>

        <article class="law-question">
          <h3>Bildrecht</h3>
          <p><strong>Zuständigkeit:</strong> Bildende Kunst, Fotografie, Grafik, Illustration, Design – inklusive Lizenzierung und Vergütungsansprüche in bildbezogenen Nutzungen.</p>
          <p><strong>Für wen?</strong> Bildende Künstler:innen, Fotograf:innen, Illustrator:innen, Designer:innen und Rechtsnachfolger:innen.</p>
          <p><a href="https://www.bildrecht.at" target="_blank" rel="noopener noreferrer">https://www.bildrecht.at</a></p>
        </article>
